namespace Eikona\Tessa\ConnectorBundle\Entity;

use DateTime;

class TessaNotificationQueue
{
    /** @var string */
    protected $type;

    /** @var string */
    protected $code;

    /** @var string */
    protected $action;

    /** @var DateTime */
    protected $created;

    /** @var DateTime */
    protected $updated;

    /**
     * @return string
     */
    public function getType(): string
    {
        return $this->type;
    }

    /**
     * @param string $type
     */
    public function setType(string $type): void
    {
        $this->type = $type;
    }

    /**
     * @return string
     */
    public function getCode(): string
    {
        return $this->code;
    }

    /**
     * @param string $code
     */
    public function setCode(string $code): void
    {
        $this->code = $code;
    }

    /**
     * @return string
     */
    public function getAction(): string
    {
        return $this->action;
    }

    /**
     * @param string $action
     */
    public function setAction(string $action): void
    {
        $this->action = $action;
    }
}
